// if else + if else

let nota = 200;

if (nota === 5) {
	console.log("Enhorabuena, has obtenido un sobresaliente");
} else if (nota === 4) {
	console.log("Buen trabajo, pero lo pudiste hacer mejor");
} else if (nota === 3) {
	console.log("Haz obtenido un suficiente");
} else if (nota === 2) {
	console.log("No haz aprobado");
} else if (nota === 1) {
	console.log("No estudiaste nada");
} else {
	console.log("Error! introduce una nota entre el 1 y el 5");
}
