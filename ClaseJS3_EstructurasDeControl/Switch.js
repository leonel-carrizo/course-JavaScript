// Sentencias Switch

let nota = 5;

switch (nota) {
	case 5:
		console.log("Buen trabajo, sobresaliente");
		break;
	case 4:
		console.log("Buen trabajo, pero lo pudiste hacer mejor");
		break;
	case 3:
		console.log("Haz obtenido un suficiente");
		break;
	case 2:
		console.log("No haz aprobado");
		break;
	case 1:
		console.log("No estudiaste nada");
		break;
	default:
		console.log("Error!");
		break;
}
